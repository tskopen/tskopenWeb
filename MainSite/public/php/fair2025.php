            <h2>Abstract</h2>
            <p>
                While concrete has high compressive strength, it is prone to cracking and fracturing under tensile forces, often indirectly through flexural forces, especially on the underside of load-bearing platforms. This leads to the degradation of civil infrastructure. This study investigates the mechanical performance of carbon fiber-reinforced concrete compared to fast-setting 50-lb Quikrete under flexural strength testing (ASTM C78–02).
            </p>
            <p>
                It was hypothesized that carbon fiber reinforcement would enhance crack resistance and slow infrastructure degradation. Alongside control samples made from 50-lb Quikrete, carbon fiber was mixed at quantities of 250 g, 150 g, and 100 g per batch. Specimens were cast following ASTM C78–02 standard ratios and tested using a hydraulic press configured to ASTM specifications.
            </p>
            <p>
                The 150 g and 100 g carbon fiber samples exhibited gradual fracture behavior, retained post-crack structural cohesion, and required additional pressure beyond initial cracking to propagate fractures. In contrast, control samples failed catastrophically at the initial fracture PSI. These findings suggest fiber-reinforced concrete reduces sudden failure risk and may lower long-term infrastructure maintenance costs.
            </p>
