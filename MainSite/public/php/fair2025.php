        <section>
            <h2>Introduction</h2>
            <p>
                This study analyzes the material properties of carbon fiber-reinforced concrete to determine possible structural uses and cost-saving benefits. While concrete is exceptionally strong in compression, it is brittle and vulnerable to tensile stress, cracking, and thermal cycling damage.
            </p>
            <p>
                Tensile strength is commonly estimated as approximately 7–10% of compressive strength, making concrete particularly vulnerable to flexural loading. Tensile forces often develop on the underside of slabs as compressive loads bend the concrete, initiating cracks that propagate upward over time.
            </p>
            <p>
                Additionally, the aerospace industry discards thousands of tons of carbon fiber annually. Repurposing this waste material offers environmental and economic benefits. Given carbon fiber’s high tensile strength and resistance to elongation, it was hypothesized that carbon fiber reinforcement would enhance crack resistance and slow infrastructure degradation.
            </p>
        </section>
        
        <section>
            <h2>Methods</h2>
            <p>
                Control specimens were cast using 50-lb Quikrete bags. Experimental batches incorporated 250 g, 150 g, and 100 g of loose carbon fiber mixed using a concrete mixer to ensure fiber dispersion.
            </p>
            <p>
                Increased water was required to maintain slump, with approximately 1 L added per 100 g of fiber. Specimens measured 4 × 4 × 14 inches, meeting ASTM C78–02 requirements.
            </p>
            <p>
                Flexural strength testing was conducted using a hydraulic press following ASTM C78–02 third-point loading configuration. Specimen dimensions were measured post-fracture to calculate modulus of rupture using ASTM-specified equations.
            </p>
        </section>
        
        <section>
            <h2>Results</h2>
            <p>
                Statistical analysis using a P-test yielded non-significant differences in PSI values between control and fiber-reinforced samples. However, qualitative observations were notable.
            </p>
            <p>
                Carbon fiber specimens fractured gradually, retained cohesion, and required additional pressure to propagate cracks. They had the characteristics of a ductile failure similar to steel reinforced concrete. Control samples failed suddenly and catastrophically, losing all structural integrity.
            </p>
        </section>
        
        <section>
            <h2>Conclusion</h2>
            <p>
                Carbon fiber-reinforced concrete reduces the risk of sudden failure, maintains residual load-bearing capacity after cracking, and slows crack propagation. These properties suggest potential reductions in infrastructure maintenance and repair costs.
            </p>
            <p>
                Future research will focus on optimizing fiber mix ratios and evaluating performance under thermal cycling conditions.
            </p>
        </section>
        
